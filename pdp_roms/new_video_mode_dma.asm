.SECTION .DATA
	COLOR0: .WORD 0000000
	SCREEN_WIDTH: .WORD 0144
	SCREEN_HEIGHT: .WORD 0454
	VRAM_OFFSET: .WORD 040000

.SECTION .CODE
.GLOBAL MAIN

MAIN:
	CLR R0
	CLR R1
	CLR R3
	MOV (VRAM_OFFSET), R2

LOOP_ST:
	MOVB R4, (R2)+

	INC R0
	CMP R0, (SCREEN_WIDTH)
	BNE LOOP_ST
	
	CLR R0
	INC R1
	CMP R1, (SCREEN_HEIGHT)
	BEQ LOOP_END

	INC R3
	CMP R3, 10
	BNE LOOP_ST

	CLR R3
	ADD 0125, R4
	CMP 0524, R4
	BNE LOOP_ST
	CLR R4
	BR LOOP_ST

LOOP_END:
	HALT