.SECTION .CODE
.GLOBAL MAIN

MAIN:
	MOV 0000000, -(R6)
	MOV 0000030, -(R6)
	MOV 0000007, -(R6)
	JSR R5, PRINT_SCREEN_CHAR_FUNCTION
	ADD 6, R6

MAIN_END:
	HALT

PRINT_SCREEN_CHAR_FUNCTION:
	# Operands: x, y, char_code
	# Old R5 value: SP+0
	# char_code: SP+2
	# y: SP+4
	# x: SP+6

	# line_printed
	MOV 0000000, -(R6)
	# vram_line_offset
	MOV (FRAME_BUFFER_OFFSET), -(R6)
	# tmp
	MOV 0000000, -(R6)

	PRINT_SCREEN_CHAR_END:
		ADD 6, R6
		RST R5
