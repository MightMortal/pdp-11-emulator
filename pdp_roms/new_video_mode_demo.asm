.SECTION .DATA
	COLOR0: .WORD 0000000
	SCREEN_WIDTH: .WORD 0620
	SCREEN_HEIGHT: .WORD 0454
	PRINT_COMMAND_OPCODE: .WORD 010002
	PRINT_COMMAND_MASK: .WORD 0167774

.SECTION .CODE
.GLOBAL MAIN

MAIN:
	CLR R0
	CLR R1
	CLR R3
	MOV (PRINT_COMMAND_OPCODE), R2

LOOP_ST:
	MOV R0, (0176304)
	MOV R1, (0176306)
	MOV R2, (0176302)

	INC R0
	CMP R0, (SCREEN_WIDTH)
	BNE LOOP_ST
	
	CLR R0
	INC R1
	CMP R1, (SCREEN_HEIGHT)
	BEQ LOOP_END
	
	INC R3
	CMP R3, 10
	BNE LOOP_ST

	CLR R3
	INC R2
	BIC (PRINT_COMMAND_MASK), R2
	BR LOOP_ST

LOOP_END:
	HALT